<?php 
    $numero1 = 5;
    $numero2 = 3;

    //maior 
    if($numero1 > $numero2){
        echo "$numero1 > $numero2". ",<br />";
    }
    //menor
    if($numero1 < $numero2){
        echo "$numero1 < $numero2". "<br />";
    }
    //maior ou igual
    if($numero1 >= $numero2){
        echo "$numero1 >= $numero2". ",<br />";
    }
    //menor ou igual
    if($numero1 <= $numero2){
        echo "$numero1 <= $numero2". ",<br />";
    }
    //igual
    if($numero1 == $numero2){
        echo "$numero1 == $numero2". ",<br />";
    }
    //diferente
    if($numero1 != $numero2){
        echo "$numero1 != $numero2". ",<br />";
    }
    //identico obs:mesmo valor e mesmo tipo
    if($numero1 === $numero2){
        echo "$numero1 === $numero2". ",<br />";
    }

?>